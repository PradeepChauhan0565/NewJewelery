        <div class="row  mb-2">
                <div class="col-12">
                        <p style="text-align:justify; color:#00263a;">
                         Jewelry isn’t just a mere accessory. It is always layered with meaning. Whether it is
                          a simple ring, bangle or an elaborate necklace, each piece is wrapped in emotions and
                           sentimentality. Engagement rings are perhaps one of the strongest memory markers.
                            It marks the beginning of a new chapter in life with your partner, a celebration 
                            of an eternal bond and a promise
                          of commitment. Wedding bands, too, are a symbol of everlasting love and devotion.  
                        </p>
                        <p style="text-align:justify; color:#00263a;">
                         There is a lot of thought and love that goes into buying these jewels. Even daily 
                         wear rings or cocktail rings can 
                         mark significant moments in your life and lift your mood in an instant.   
                        </p>
                        <p style="text-align:justify; color:#00263a;">
                        If you are looking out for a diamond ring of any kind, here are some points
                         to remember so that you can shop like a pro. 
                        </p>
                     

                        <h4 class="my-4" style="color:#00263a;">Budget</h4>
                <p style="text-align:justify; color:#00263a;">
                  You should set your own budget and not follow societal norms of any kind
                   such as spending three months of your salary on an engagement ring. At the
                    end of the day, you should be happy 
                  with your purchase and the joy that it brings to your life is priceless.    
                   
                </p>
                        <h4 class="my-4" style="color:#00263a;">Cut over carat</h4>
                <p style="text-align:justify; color:#00263a;">
                When it comes to determining the price of the diamond ring, a diamond’s cut, color,
                 clarity and carat play an integral role. The cut of the diamond has the biggest 
                 impact on the cost and on the level of the sparkle as well. This not only refers to
                  the shape of the diamond but also how well it is cut so that the facets interact 
                  with light to the fullest.  Factors such as the diamond’s proportions, 
                symmetry and polish determine the fire, scintillation and brilliance of the stone.   
                   
                </p>

                 <p style="text-align:justify; color:#00263a;">
                 While buying a diamond, pay attention to the cut. It is more important than 
                 the carat weight because a poorly cut larger
                  diamond wont sparkle as much as a smaller diamond with an excellent cut. 
                </p>

                <p style="text-align:justify; color:#00263a;">
                 The most popular diamond cut across categories is a round brilliant diamond because its
                  fire and brilliance outshine any other cut. They make up for almost 75 percent of all 
                  the diamond engagement rings sold in USA.
                  It is also versatile enough to be set in a number of settings styles. 
                </p>
                <p style="text-align:justify; color:#00263a;">
               If you are looking at a fancy cut, oval diamond rings are gaining popularity because
                they sparkle almost as much as round brilliant cut solitaire but tend to show more 
                inclusions. <a href="https://www.kamajewelry.com/details/78ct-horizontal-oval-fancy-ring-JRZ889008">Oval diamond rings</a>, like this one, look bigger
                than the actual carat weight and make the fingers look longer.
                </p>
                <p style="text-align:justify; color:#00263a;">
              Cushion cut diamonds are super sparkly and help hide inclusions. The most affordable cuts
               are radiant cuts which are square or rectangular brilliant-cut diamonds with cut corners.
                </p>

             
                <p style="text-align:justify; color:#00263a;">
                 Don’t focus only on benchmark carat weights. If you want to save some money on the cost, 
                 there is a widely known trick of buying 
                 shy of whole carat marks without making much difference to the overall look of the stone.  
                   
                </p>
                

                 <h4 class="my-4" style="color:#00263a;">The right setting</h4>
                <p style="text-align:justify; color:#00263a;">
                 The right setting can help enhance the sparkle of a diamond. The number one setting for a 
                 round cut diamond ring is the four-prong setting. 
                 Apart from being simple and elegant, the setting allows maximum light to enter the stone.  
                   
                </p>
                <p style="text-align:justify; color:#00263a;">
                 Bezel setting is the next most popular setting style. They offer excellent protection 
                 to the diamond making it an apt choice for those with an active lifestyle.
                  Smaller diamonds that frame the central stone like a halo, offer a bigger look. 
                </p>
                <p style="text-align:justify; color:#00263a;">
          Two- and <a href="https://www.kamajewelry.com/details/1-15-twt-round-three-stone-vintage-ring-in-14kt-JRW02581F">three stone setting</a>
           allows you to play with more diamonds or perhaps mix it up with other
           gemstones. When paired smartly, it enhances the appearance of the central stone.  
                   
                </p>
               
                <p style="text-align:justify; color:#00263a;">
                 If you want even sparkle, consider an eternity band with pave set diamonds or a 
                 ring whose shank is accented with pave diamonds for added glitter and shine. 
                   
                </p>
                <p style="text-align:justify; color:#00263a;">
                 <a href="https://www.kamajewelry.com/details/100ct-channel-set-with-diamonds-on-the-sides-anniversary-band-JRA065510">Channel-set diamond</a>
                  rings can hide variations in diamond sizes. 
                 It minimizes imperfections and maximizes small stones. 
                 The only downside is that one needs to take care that dirt doesn’t settle between the diamonds.
                </p>
                
                  <h4 class="my-4" style="color:#00263a;">Choice of metal</h4>
                <p style="text-align:justify; color:#00263a;">
                If you have the budget, platinum is the best metal for a diamond ring, especially if you are
                using bigger stones because the metal holds on to the stones more securely than gold.
                 That being said, white, yellow and rose gold rings are also popular. Rose gold is not 
                 suited for those who are allergic to copper. Although 14-karat has less gold content
                 it is more durable than 18k
                 gold, making it a better choice for those who are always working with their hands. 
                   
                </p>

                 <h4 class="my-4" style="color:#00263a;">Which finger does one wear an engagement ring on? </h4>
                <p style="text-align:justify; color:#00263a;">
                The ancient Romans believed that a vein on the third finger on the left hand went straight 
                to the heart. It was called Vena Amoris 
                meaning the ‘vein of love’. Many wear their engagement ring on this finger. 
                   
                </p>
                 <p style="text-align:justify; color:#00263a;">
             Others in Russia, Germany, Norway and India consider the left hand unlucky or sinister. 
             Over here, they wear it
              on the right hand. Cultural notions aside, it is all about personal preference.  
                   
                </p>
                 <h4 class="my-4" style="color:#00263a;">Stackable rings</h4>
                <p style="text-align:justify; color:#00263a;">
              Many people like stacking their engagement and wedding rings. Some make things easier by
               buying both in one go so that they don’t have the hassle of finding something that twins
                well with their engagement ring at a later date. This <a href="https://www.kamajewelry.com/shop/RINGS/ENGAGEMENT+RING/Bridal+Set">1 carat round composite bridal diamond set of rings </a>
                 is an ideal choice for those who want such a pair.   
                   
                </p>
                <p style="text-align:justify; color:#00263a;">
             There are plenty of diamonds and setting styles. Don’t be flustered by the wide 
             array of choices. Pick one that resonates with your soul. 
             The right one will sit right at home in your heart and mind.    
                   
                </p>

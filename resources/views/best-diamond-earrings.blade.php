                        <h4 class="my-4" style="color:#00263a;">Budget</h4>
                <p style="text-align:justify; color:#00263a;">
                   For starters, decide your budget beforehand. There is no point getting stuck on a
                    grand piece that is over the price you have set aside for your purchase. 
                   It also helps you narrow your search and gives you a good starting point.    
                   
                </p>
                        <h4 class="my-4" style="color:#00263a;">Diamond Quality</h4>
                <p style="text-align:justify; color:#00263a;">
                  While design is important, the quality of each diamond in your
                   earring is an important point of consideration.    
                   
                </p>

                 <p style="text-align:justify; color:#00263a;">
                 Cut: The cut of the diamond impacts the sparkle and brilliance of a diamond. It not only
                  refers to the shape in which the diamond is cut but also the stone’s proportion, depth and 
                 symmetry. A round diamond with an excellent cut grade has the maximum sparkle.   
                </p>

                <p style="text-align:justify; color:#00263a;">
                 Color: Diamonds run from colorless (graded D) to near-colorless with slight hints of
                  yellow and brown (Z grade). Consider 
                 getting a near-colorless diamond that is less expensive than a colorless diamond.  
                </p>
                <p style="text-align:justify; color:#00263a;">
                Clarity: This determines the level of inclusions and imperfections in the stone. Since most
                 people will view your earrings from a distance, you can opt for a diamond that has a lower 
                 clarity than if you were buying a 
                diamond ring. This will allow you to spend a little more on the other Cs.
                </p>
                <p style="text-align:justify; color:#00263a;">
               Carat: The bigger the diamond does not necessarily translate to a better one. While diamond 
               size in important, a poorly 
               cut big diamond will lack the luster of a smaller diamond with a better cut.
                </p>

                 <h4 class="my-4" style="color:#00263a;">Metal Tone</h4>
                <p style="text-align:justify; color:#00263a;">
                  The next point of consideration is the metal and tone of the metal. Do you want to 
                  shine bright with yellow gold or want something more complimentary like white gold
                   for your earrings?
                   You could opt for a lower cartage like 14K for your earrings to bring the cost down.    
                   
                </p>
                <p style="text-align:justify; color:#00263a;">
                  Platinum is also an option specially for larger gemstones since it is a denser metal.    
                   
                </p>


                        <h4 class="mb-3">There are two widely used manufacturing processes:</h4>
                    
                    
                        <div class="d-flex">
                            <div>&#11044;</div>
                            <p style="margin-left:10px; text-align:justify; color:#00263a;">
                            The first method uses high pressure and high temperature (HPHT) growth to recreate
                            the conditions in which carbon crystallizes into diamond. This process is being primarily 
                            used for creating diamonds 
                            having a size of a half carat or smaller and is used in watches and jewelry. 
                            </p>
                        </div>

                        <div class="d-flex">
                            <div>&#11044;</div>
                            <p style="margin-left:10px; text-align:justify; color:#00263a;">
                           The second method is chemical vapor deposition (CVD) which uses hydrocarbon gases
                             injected into a growth chamber. Microwave plasma energy is used to break apart the bond 
                             of the hydrocarbon, allowing free carbon atoms to shower down on a plate of diamonds. Atom by 
                             atom, this gradually grows a diamond vertically, so you end up with the same high purity and
                              high-quality diamonds as earth-mined diamonds. This process is being primarily used for creating
                             diamonds having a size of a half carat or larger and is being used for high end jewelry.
                            </p>

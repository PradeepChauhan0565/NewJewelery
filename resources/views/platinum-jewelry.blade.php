                <h4 class="my-4" style="color:#00263a;">Why is platinum expensive?</h4>
                 <p style="text-align:justify; color:#00263a;">
                       Platinum is a hard metal and requires a high melting point for detailed carving and milling.
                        Only skilled craftsmen can work with platinum. It also required expensive manufacturing
                         facilities. This results in higher working costs. Also, there is no standard price for 
                         platinum like gold.
                        Prices may vary for the same quantity and quality of metal from one jeweler to another.    
                </p>
                <h4 class="my-4" style="color:#00263a;">Platinum versus white gold  </h4>
                 <p style="text-align:justify; color:#00263a;">
                     Platinum is a naturally occurring white metal that does not use its luster or color over time. 
                     White gold on the other hand is an alloy of yellow gold and other metals. While it looks similar,
                      white gold may lose its sheen over time and will have to be re-plated.
                      Platinum retains its sheen. It is denser and almost 40% heavier than gold.  
                </p>
